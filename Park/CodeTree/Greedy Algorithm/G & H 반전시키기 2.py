# 변수 선언 및 입력

n = int(input())  # 문자열의 길이 입력
a = input()       # 첫 번째 문자열 입력
b = input()       # 두 번째 문자열 입력

# 뒤에서부터 문자를 확인하며 조건을 만족하는 경우에 동작을 수행.
# cnt는 선택한 횟수를 셉니다.
cnt = 0

# 문자열의 끝에서부터 앞으로 이동하며 두 문자열을 비교.
for i in range(n - 1, -1, -1):
    # a[i]와 b[i]가 다르면서, 지금까지의 선택 횟수가 짝수라면
    # 현재 위치의 차이를 해소하기 위해 선택.
    if a[i] != b[i] and cnt % 2 == 0:
        cnt += 1  # 선택을 했으니 횟수 증가

    # a[i]와 b[i]가 같으면서, 지금까지의 선택 횟수가 홀수라면
    # 이전 선택으로 인해 현재 문자가 영향을 받고 있는 경우이므로
    # 다시 한 번 선택하여 원래대로 되돌린다.
    elif a[i] == b[i] and cnt % 2 == 1:
        cnt += 1  # 선택을 했으니 횟수 증가

# 최종 선택 횟수를 출력
print(cnt)
