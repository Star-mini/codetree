arr = [0, 3, 6, 2, 6, 7, 5, 2]  # 입력 배열, 0번 인덱스는 사용하지 않습니다.
L = [0] * 8  # L 배열 초기화
R = [0] * 8  # R 배열 초기화
n = 7  # 배열의 크기

# L 배열 채우기
L[1] = 0
for i in range(2, n + 1):
    L[i] = L[i - 1] + abs(arr[i] - arr[i - 1])  # 인접 원소 간 차이의 절대값을 더하기

# R 배열 채우기
R[n] = 0
for i in range(n - 1, 0, -1):
    R[i] = R[i + 1] + abs(arr[i + 1] - arr[i])  # 인접 원소 간 차이의 절대값을 더하기

# 질의 예시
# 4번째 숫자를 제외했을 때
print(L[3] + R[5] + abs(arr[5] - arr[3]))  # 출력: 17
# 5번째 숫자를 제외했을 때
print(L[4] + R[6] + abs(arr[6] - arr[4]))  # 출력: 15
